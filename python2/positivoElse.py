"""

dado un numero entero este programa determina si es positivo o negativo
    

"""

numero = int (input ("Valor numerico : " ))

if numero > 0 : 
    print (f"El numero {numero} es positivo")
elif numero == 0 :
    print (f"El numero es {numero}")
else: 
    print(f"El numero {numero} es negativo")
    


